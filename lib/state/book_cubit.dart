  void sortBy(SortMode mode) {
    _applySortAndEmit(state.books, mode, toListView: true);
  }

  void _applySortAndEmit(List<Book> current, SortMode mode,
      {bool toListView = false}) {
    final books = List<Book>.from(current);
    if (mode == SortMode.author) {
      books.sort((a, b) => a.author.toLowerCase().compareTo(b.author.toLowerCase()));
    } else {
      books.sort((a, b) => a.title.toLowerCase().compareTo(b.title.toLowerCase()));
    }
    emit(state.copyWith(
      books: books,
      sortMode: mode,
      view: toListView ? ViewMode.list : state.view,
      selected: toListView ? null : state.selected,
      loading: false,
    ));
  }

  void selectBook(Book book) {
    emit(state.copyWith(view: ViewMode.detail, selected: book));
  }

  void backToList() {
    emit(state.copyWith(view: ViewMode.list, selected: null));
  }

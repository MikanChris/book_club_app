import 'package:flutter_bloc/flutter_bloc.dart';
import '../models/book.dart';

enum SortMode { author, title }
enum ViewMode { list, detail }

class BookState {
  final List<Book> books;
  final SortMode sortMode;
  final ViewMode view;
  final Book? selected;
  final bool loading;

  const BookState({
    required this.books,
    required this.sortMode,
    required this.view,
    this.selected,
    this.loading = false,
  });

  BookState copyWith({
    List<Book>? books,
    SortMode? sortMode,
    ViewMode? view,
    Book? selected,
    bool? loading,
  }) {
    return BookState(
      books: books ?? this.books,
      sortMode: sortMode ?? this.sortMode,
      view: view ?? this.view,
      selected: selected ?? this.selected,
      loading: loading ?? this.loading,
    );
  }
}

